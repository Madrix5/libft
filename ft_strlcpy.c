#include "libft.h"

int	ft_strlcpy(char *dest, const char *src, size_t destsize)
{
	int	i;

	i = 0;
	while (src[i] != '\0')
	{
		dest[i] = src[i];
		i++;
	}
	dest[i] = '\0';
	return (i);
}
